

-- CARGA DE TABLA FAC_DEUDAS

SET ECHO ON
SET TIMING ON
SET FEEDBACK ON

SPOOL &1;


TRUNCATE TABLE FAC_DEUDAS;

INSERT INTO FAC_DEUDAS
(
	CODMES                 , 
	CODSBSCLIENTE          ,
	CODENTIDAD             ,
	CODPRODUCTO            ,
	CODSUBPRODUCTO         ,
	DEUDACALIFCLIENTE      ,
	CLASIFDEUDACLIENTE     ,
	SALDO                  ,
	FECCARGATABLA           
)
SELECT 
	A.CODMES              ,
	A.CODSBSCLIENTE       ,
	A.CODENTIDAD          ,
	A.CODPRODUCTO         ,
	A.CODSUBPRODUCTO      ,
	C.DEUDACALIFCLIENTE   ,
	C.CLASIFDEUDACLIENTE  ,
	A.SALDO               ,
	SYSDATE	          
FROM HM_DEUDACLIENTE A
LEFT JOIN HM_CLIENTE C
ON A.CODSBSCLIENTE = C.CODSBSCLIENTE 
AND A.CODMES = C.CODMES;

COMMIT;

DISCONNECT;
EXIT;